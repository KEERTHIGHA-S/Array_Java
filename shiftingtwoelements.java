public class shiftingtwoelements
{
    public static void main(String[] args)
    {
        int a[]={10,20,30,40,50,60,70,80};
        int i=0;
        while(i<a.length)
        {
            if(i>=2)
            {
                System.out.println(a[i]);
               
            } i++;
        }
            int j=0;
          while(j<a.length)
          {
              if(j<2)
              {
                  System.out.println(a[j]);
              }j++;
          }
        }
    }